# Descrição:
# Calcula a quantidade de latas de tintas necessárias para pintar uma área.

print('=' * 40 + 'LOJA DE TINTAS' + '=' * 40)
print('Bem-vindo(a) ao programa ' + TITULO)
print('Olá! Vamos te ajudar a descobrir quantas latas de tinta comprar baseado no metro quadrado a realizar a pintura!')
largura = float(input('Informe a largura da área em metros: '))
altura = float(input('Agora informe a altura da área em metros: '))
metro = largura * altura
latas_inteiro = metro // 3
latas_resto = metro % 3 > 0
latas_total = latas_inteiro + latas_resto
print('=' * 30 + 'CALCULANDO' + '=' * 30)
print('A quantidade de tinta para {}m² é o equivalente à {:.0f} latas'.format(metro, latas_total))
print('=' * 70)
